    {
        $data = $request->input();
        DB::table('tittle')->insert([
            'tittle' => $data['tittle_name'],
            'ticket' => $data['ticket'],
            'answer' => '',
            'ynAnswer' => false,
            'user_name' => $data['user_name']
        ]);
        return redirect()->route('login');
    }

